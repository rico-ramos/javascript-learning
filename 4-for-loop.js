// For Loop - Counter
//for(declaration, condition, counter)

for(let counter=0; counter<10; counter++){
    console.log("the count ", counter);
}


// For Loop - Classic Countdown
for(let revCount=5; revCount>0; revCount--){
    console.log("Launching in ..."+ revCount);
}
console.log("Blast off!...")

// For Loop - Infinity
for(let revCount=5; true; revCount--){
    console.log("Launching in ..."+ revCount);
}
console.log("Blast off!...")